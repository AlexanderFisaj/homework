// начинаем расчет максимального количества ягод со смежных кустов
maxim = raspberry[0]+raspberry[1]+raspberry[2];

while (j < raspberry.Length)
{
    sbor = raspberry[j-2]+raspberry[j-1]+raspberry[j];
    if (sbor > maxim)
        maxim = sbor;    
    j++;
}

Console.WriteLine($"Максимальное число ягод = {maxim}");
